<h2>Conquer Doddabetta Mountain</h2>
<p>Doddabetta (Tamil pronunciation: [toʈːabeʈːaː]) is the highest mountain in the Nilgiri Mountains at 2,637 metres (8,652 ft). There is a reserved forest area around the peak. It is 9 km from Ooty, on the Ooty-Kotagiri Road in the Nilgiris District of Tamil Nadu, India. It is a popular tourist attraction with road access to the summit. It is the fourth highest peak in South India next to Anamudi, Mannamalai and Meesapulimala. The peaks Hecuba (2375 m), Kattadadu (2418 m) and Kulkudi (2439 m) are the three closely linked summits in the west of the Doddabetta range near to Udagamandalam. The word Doddabetta is derived from Kannada language meaning big hill.</p>
